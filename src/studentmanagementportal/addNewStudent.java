    private void fatherInputKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fatherInputKeyReleased
        String PATTERN ="[a-zA-Z](0,30)$";
        Pattern patt =Pattern.compile(PATTERN);
        Matcher match =patt.matcher(fatherInput.getText());
        if(!match.matches())
            flab.setText("Invalid name");
        else
          flab.setText("");
    }//GEN-LAST:event_fatherInputKeyReleased

