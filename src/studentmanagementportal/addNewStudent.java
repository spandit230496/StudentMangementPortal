    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        String courses=(String)courseInput.getSelectedItem();
        String branch=(String)branchInput.getSelectedItem();
        String rollno=redgNo.getText();
        String name=nameInput.getText();
        String fathername=fatherInput.getText();
        String gender =(String)genderSelect.getSelectedItem();
        
        
        if(name.equals("")&&fathername.equals("")&&rollno.equals(""))
        {  JOptionPane.showMessageDialog(null,"Field Can't be empty");
            
        }
      else if(name.equals("")&&!fathername.equals("")&&!rollno.equals(""))
        {  JOptionPane.showMessageDialog(null,"Name Can't be empty");
            
        }
       else if(!name.equals("")&&fathername.equals("")&&!rollno.equals(""))
        {  JOptionPane.showMessageDialog(null," Father Name Can't be empty");
            
        }
        else if(!name.equals("")&&!fathername.equals("")&&rollno.equals(""))
        {  JOptionPane.showMessageDialog(null," Father Name Can't be empty");
            
        }
         else if(name.equals("")&&fathername.equals("")&&!rollno.equals(""))
        {  JOptionPane.showMessageDialog(null,"Name  and Father Name Can't be empty");
            
        }
         else if(!name.equals("")&&fathername.equals("")&&rollno.equals(""))
        {  JOptionPane.showMessageDialog(null,"Father  and Roll No Can't be empty");
            
        }
         else if(name.equals("")&&!fathername.equals("")&&!rollno.equals(""))
        {  JOptionPane.showMessageDialog(null,"Name  and Roll No Can't be empty");
            
        }
            
         else{
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con =DriverManager.getConnection("jdbc:mysql://localhost:3306/student","root","210396");
            Statement st=con.createStatement();
            st.executeUpdate("insert into student values('"+rollno+"','"+courses+"','"+branch+"','"+name+"','"+gender+"','"+fathername+"')");
            JOptionPane.showMessageDialog(null,"Sucessfully added");

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
        }
        
        
        

    }//GEN-LAST:event_saveBtnActionPerformed

